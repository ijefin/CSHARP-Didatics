using System;

class Pessoa
{
    private string nome;
    private int idade;
    private string profissao;

    public string Nome
    {
        get { return nome; }
        set { nome = value; }
    }

    public int Idade
    {
        get { return idade; }
        set { idade = value; }
    }
    public string Profissao
    {
        get { return profissao; }
        set { profissao = value; }
    }

}